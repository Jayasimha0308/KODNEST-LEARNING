package com.kodnest.loops;

public class ForInDowhile {

	public static void main(String[] args) {
		 do
		    {
		    	System.out.println("Hello");
		    	for(int i=1; i<=100; i++)
		    	{
		    		System.out.println("Kodnest");
		    		if(i==19)
		    		{
		    			break;
		    		}
		    	}
		    	System.out.println("Technologies");
		    	break;
		    }while(true);
	}

}
